void main() {
  int dayOfWeek = 3;
  switch (dayOfWeek) {
    case 0:
      print("sun");
      break;
    case 1:
      print("mon");
      break;
    case 2:
      print("tue");
      break;
    case 3:
      print("wed");
      break;
    case 4:
      print("thu");
      break;
    case 5:
      print("fri");
      break;
    case 6:
      print("sat");
      break;
    default:
      print("Not a day of week");
      break;
  }
}
